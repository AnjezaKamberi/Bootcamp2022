package al.academy.ikubinfo.bootcamp.seanca6;

public class Student {

	private int[] nota;
	private String[] lende;
	private String dege;

	public int[] getNota() {
		return nota;
	}

	public void setNota(int[] nota) {
		this.nota = nota;
	}

	public String[] getLende() {
		return lende;
	}

	public void setLende(String[] lende) {
		this.lende = lende;
	}

	public String getDege() {
		return dege;
	}

	public void setDege(String dege) {
		this.dege = dege;
	}

}
